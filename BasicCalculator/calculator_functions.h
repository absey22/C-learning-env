#include <stdio.h>


float mult(float a, float b)
{
  float result;
  

  result = a*b;

  return(result);
}

float divi(float a, float b)
{
  float result;
  

  result = a/b;

  return(result);
}

float addi(float a, float b)
{
  float result;
  

  result = a+b;

  return(result);
}

float subt(float a, float b)
{
  float result;
  

  result = a-b;

  return(result);
}
